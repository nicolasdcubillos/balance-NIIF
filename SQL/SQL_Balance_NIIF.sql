CREATE FUNCTION [dbo].[X_SALDOCUENTA_NIIF_CC]
(
@fecha date,
@cuenta varchar(20),
@codcc varchar(20)
)
RETURNS NUMERIC(17,2)
AS
	BEGIN
	DECLARE @valor NUMERIC(17,2)
	SELECT @valor = (
	SELECT ISNULL(SUM (TEMP.DEBITO) - SUM(TEMP.CREDITO), 0) SALDO_INICIAL FROM
	(
		SELECT
		DATEFROMPARTS(CAST(ANO AS INT),
		CASE WHEN PERIODO = 13 THEN 12 ELSE CAST (PERIODO AS INT) END, 1) FECHA, *
		FROM SALDCONTNI) TEMP
		WHERE TEMP.CODIGOCTA LIKE RTRIM(@cuenta) + '%' AND TEMP.CODCC = @codcc AND TEMP.FECHA < @fecha
	)
	RETURN @valor
END

GO

CREATE FUNCTION [dbo].[X_BALANCE_CC]
(
	@codcc varchar(20),
	@codigocta varchar(20),
	@ano numeric,
	@periodo numeric
)
RETURNS TABLE 
AS
RETURN
(
	SELECT 
	SALDCONTNI.CODIGOCTA, 
	CUENTASNIF.DESCRIPCIO NOMBRECTA,
	SALDCONTNI.CODCC,
	dbo.X_SALDOCUENTA_NIIF_CC(DATEFROMPARTS(CAST(@ano AS INT), CAST(@periodo AS INT), 1), SALDCONTNI.CODIGOCTA, CODCC) SALDO_INICIAL,
	SUM(DEBITO) DEBITO, 
	SUM(CREDITO) CREDITO,
	1 AUXILIAR
	FROM 
	SALDCONTNI,
	CUENTASNIF
	WHERE 
	CODCC LIKE RTRIM(@codcc) + '%' AND
	SALDCONTNI.CODIGOCTA LIKE RTRIM(@codigocta) + '%' AND
	DATEFROMPARTS(CAST(ANO AS INT), CAST(CASE WHEN PERIODO = 13 THEN 12 ELSE PERIODO END AS INT), 1) < DATEFROMPARTS(CAST(@ano AS INT), CAST(@periodo AS INT), 1) AND
	CUENTASNIF.CODIGOCTA = SALDCONTNI.CODIGOCTA
	GROUP BY 
	SALDCONTNI.CODIGOCTA, SALDCONTNI.CODCC, CUENTASNIF.DESCRIPCIO
	UNION ALL
	SELECT 
	C.CODIGOCTA,
	C.DESCRIPCIO,
	'',
	dbo.X_SALDOCUENTA_NIIF_CC(DATEFROMPARTS(CAST(@ano AS INT), CAST(@periodo AS INT), 1), C.CODIGOCTA, RTRIM(@codcc)) SALDO_INICIAL,
	ISNULL((SELECT SUM(DEBITO) from SALDCONTNI
	WHERE CODIGOCTA LIKE RTRIM(C.CODIGOCTA) + '%' and CODCC LIKE RTRIM(@CODCC) + '%' and DATEFROMPARTS(CAST(ANO AS INT), CAST(CASE WHEN PERIODO = 13 THEN 12 ELSE PERIODO END AS INT), 1) < DATEFROMPARTS(CAST(@ano AS INT), CAST(@periodo AS INT), 1)), 0),
	ISNULL((SELECT SUM(CREDITO) from SALDCONTNI
	WHERE CODIGOCTA LIKE RTRIM(C.CODIGOCTA) + '%' and CODCC LIKE RTRIM(@CODCC) + '%' and DATEFROMPARTS(CAST(ANO AS INT), CAST(CASE WHEN PERIODO = 13 THEN 12 ELSE PERIODO END AS INT), 1) < DATEFROMPARTS(CAST(@ano AS INT), CAST(@periodo AS INT), 1)), 0),
	0
	FROM 
	CUENTASNIF C
	WHERE 
	TIPOCUENTA = 0 AND
	CODIGOCTA LIKE RTRIM(@CODIGOCTA) + '%'
)

